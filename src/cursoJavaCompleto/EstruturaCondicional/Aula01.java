package cursoJavaCompleto.EstruturaCondicional;

public class Aula01 {
    //ExpressÃµes comparativas
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;

        // > maior que
        System.out.println(num1 > num2); // false

        // < menor que
        System.out.println(num1 < num2); // true

        // >= maior ou igual a
        System.out.println(num1 >= num2); // false

        // <= menor ou igual a
        System.out.println(num1 <= num2); // false

        // == igual a
        System.out.println(num1 == num2); // false

        //!= diferente de
        System.out.println(num1!= num2); // true
    }
}
