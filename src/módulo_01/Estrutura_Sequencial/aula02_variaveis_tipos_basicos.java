package módulo_01.Estrutura_Sequencial;

public class aula02_variaveis_tipos_basicos {
    public static void main(String[] args) {
        // Variaveis e tipos básicos em Java
        int idade = 27; // tipo inteiro
        double altura = 1.80; // tipo ponto flutuante
        char letra = 'C'; // tipo caractere
        boolean estudante = true; // tipo booleano
        String nome = "Caique"; // tipo String (sequência de caracteres)

        System.out.println("Nome: " + nome
                + "\nIdade: " + idade
                + "\nAltura: " + altura
                + "\nLetra: " + letra
                + "\nEstudante: " + estudante);
        
        // um BIT pode armazenar 2 valores possíveis (0 ou 1)
        // um BYTE é composto por 8 BITS e pode armazenar 256 valores possíveis
    }
}
